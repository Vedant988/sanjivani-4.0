/**
 * @file errorHandler.js
 * @description Global error handling middleware and async wrapper.
 * Ensures consistent error responses and simplifies async route handling.
 */

/**
 * Global error handling middleware.
 * 
 * Intercepts errors passed to `next(err)`, formats them into a standard JSON response,
 * and handles specific Mongoose error types (CastError, Duplicate Key, ValidationError).
 * 
 * @function errorHandler
 * @param {Error} err - The error object
 * @param {import('express').Request} req - Express request object
 * @param {import('express').Response} res - Express response object
 * @param {import('express').NextFunction} next - Express next function
 * @returns {void}
 */
export const errorHandler = (err, req, res, next) => {
  let error = { ...err };
  error.message = err.message;

  // Log error for debugging
  console.error('Error:', err);

  // Mongoose bad ObjectId
  if (err.name === 'CastError') {
    const message = 'Resource not found';
    error = { message, statusCode: 404 };
  }

  // Mongoose duplicate key
  if (err.code === 11000) {
    const field = Object.keys(err.keyPattern)[0];
    const message = `${field.charAt(0).toUpperCase() + field.slice(1)} already exists`;
    error = { message, statusCode: 400 };
  }

  // Mongoose validation error
  if (err.name === 'ValidationError') {
    const message = Object.values(err.errors).map(val => val.message).join(', ');
    error = { message, statusCode: 400 };
  }

  // Default error
  const statusCode = error.statusCode || 500;
  const message = error.message || 'Server Error';

  res.status(statusCode).json({
    success: false,
    error: message,
    ...(process.env.NODE_ENV === 'development' && { stack: err.stack })
  });
};

/**
 * Higher-order function to wrap async route handlers.
 * 
 * Automatically catches errors thrown in async functions and passes them to the
 * global error handler using `next()`. Eliminates the need for try-catch blocks
 * in every controller.
 * 
 * @function asyncHandler
 * @param {Function} fn - The async route handler function
 * @returns {Function} Express middleware function
 */
export const asyncHandler = (fn) => (req, res, next) => {
  Promise.resolve(fn(req, res, next)).catch(next);
};

